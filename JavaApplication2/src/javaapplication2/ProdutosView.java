    public void ManipulaInterfaceDep() {

        switch (modoDep) {

            case "Novo":
                jbCadastrar.setEnabled(true);
                jbLimpar.setEnabled(true);
                jLimpa.setEnabled(false);

                jLoad.setEnabled(false);
                jPesquisar.setEnabled(false);
              
                jEscolha.setEnabled(false);
                jDesc.setEnabled(false);

                jCod.setEnabled(true);
                jDesconto.setEnabled(true);

                jValorCusto.setEnabled(true);
                jMarca.setEnabled(true);

                jValorVenda.setEnabled(true);
                jGrupo.setEnabled(true);
                jDesc.setEnabled(true);

                jLucro.setEnabled(true);

                jbNovo.setEnabled(false);

                jSalvar.setEnabled(false);
                jExcluir.setEnabled(false);

                break;

            case "Editar":
                jbCadastrar.setEnabled(false);
                jbLimpar.setEnabled(true);

                jLoad.setEnabled(false);
                jPesquisar.setEnabled(false);
               
                jEscolha.setEnabled(false);
                jDesc.setEnabled(false);

                jCod.setEnabled(true);
                jDesconto.setEnabled(true);

                jValorCusto.setEnabled(true);
                jMarca.setEnabled(true);

                jValorVenda.setEnabled(true);
                jGrupo.setEnabled(true);
                jDesc.setEnabled(true);

                jLucro.setEnabled(true);

                jExcluir.setEnabled(false);
                jbNovo.setEnabled(false);
                jLimpa.setEnabled(false);

                jSalvar.setEnabled(true);

                break;

            case "Excluir":
                jbNovo.setEnabled(true);
                jbCadastrar.setEnabled(false);
                jbLimpar.setEnabled(false);
                jLimpa.setEnabled(false);
                jDesc.setEnabled(false);

                jCod.setEnabled(false);
                jDesconto.setEnabled(false);

                jValorCusto.setEnabled(false);
                jMarca.setEnabled(false);

                jValorVenda.setEnabled(false);
                jGrupo.setEnabled(false);
                jDesc.setEnabled(false);

                jLucro.setEnabled(false);

                jSalvar.setEnabled(false);
                jExcluir.setEnabled(false);

                break;

            case "Selecao":
                jbCadastrar.setEnabled(false);
                jbLimpar.setEnabled(false);
                jbNovo.setEnabled(true);
                jDesc.setEnabled(false);
                jCod.setEnabled(false);
                jDesconto.setEnabled(false);

                jValorCusto.setEnabled(false);
                jMarca.setEnabled(false);

                jValorVenda.setEnabled(false);
                jGrupo.setEnabled(false);
                jDesc.setEnabled(false);

                jLucro.setEnabled(false);

                jSalvar.setEnabled(false);
                jLimpa.setEnabled(true);
                jExcluir.setEnabled(true);

                jLoad.setEnabled(true);
                jPesquisar.setEnabled(true);
                
                jEscolha.setEnabled(true);
                break;

            default:
                JOptionPane.showMessageDialog(null, "Modo inválido");
        }
    }

