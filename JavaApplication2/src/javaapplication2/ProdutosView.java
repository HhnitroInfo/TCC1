    private void jExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jExcluirActionPerformed
        String sql = "SELECT * FROM produto WHERE codigo='"
                + jCod.getText() + "'";
        BD.setResultSet(sql);
        String nom = "";
        try {
            BD.resultSet.next();
            nom = "Deletar o Produto:" + BD.resultSet.getString("codigo");
        } catch (SQLException ex1) {
            JOptionPane.showMessageDialog(null, "Produto não cadastrado!");
            jCod.requestFocus();
            BD.setResultSet("SELECT * FROM produto");
            return;
        }
        int n
                = JOptionPane.showConfirmDialog(null, nom, "", JOptionPane.YES_NO_OPTION);
        if (n == JOptionPane.YES_OPTION) {
            sql = "DELETE FROM produto WHERE codigo='" + jCod.getText() + "'";
            int r = BD.runSQL(sql);
            if (r == 1) {
                JOptionPane.showMessageDialog(null, "Exclusão realizada com sucesso!");
            } else {
                JOptionPane.showMessageDialog(null, "Não foi possível excluir o Produto!");

            }
        }
        BD.setResultSet("SELECT * FROM produto");
    }//GEN-LAST:event_jExcluirActionPerformed

